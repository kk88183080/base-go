package main

import "fmt"

func main() {

	fmt.Println(3^2+4^2 == 5^2)

	//在 Go 语言里面，^ 作为二元运算符时表示按位异或：对应位，相同为 0，相异为 1
	//0011 ^ 0010 == 0001   (3^2 == 1)
	//0100 ^ 0010 == 0110   (4^2 == 6)
	//0101 ^ 0010 == 0111   (5^2 == 7)

	fmt.Println(6^2+8^2 == 10^2) // false
	fmt.Println(6 ^ 2 + 8 ^ 2)   // false
	fmt.Println(10 ^ 2)          // false

	// 0000 0
	// 0001 1
	// 0010 2
	// 0011 3
	// 0100 4
	// 0101 5
	// 0110 6
	// 0111 7
	// 1000 8
	// 1001 9
	// 1010 10

	// 0110 ^ 0010 = 0100 =4
	// 1000 ^ 0010 = 1010 =10
	// 1010 ^ 0010 = 1000 =8
}
